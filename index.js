const choice = (array) => {
    /*
        choose a random element from a list
     */
    let uniqueArray = Array.from(new Set(array));
    if(uniqueArray.length === 0){
        return null;
    }
    let randomIndex = Math.floor(Math.random() * uniqueArray.length);
    return uniqueArray[randomIndex];
};

